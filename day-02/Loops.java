/**
 * Loops
 */
public class Loops {

  public static void main(String[] args) {
    // loop - while, do while, for
    int i = 1;
    // while
    while (i < 5) {
      System.out.println("Value of i is: " + i);
      i++;
    }

    // for
    for(int j =1; j<=5; j++){
      System.out.println("Day "+j);
    }
  }
}
